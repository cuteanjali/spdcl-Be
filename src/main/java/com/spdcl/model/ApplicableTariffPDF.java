package com.spdcl.model;

public class ApplicableTariffPDF {

	private String applicableTariff;
	private String checking;
	private String status;
	
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getApplicableTariff() {
		return applicableTariff;
	}
	public void setApplicableTariff(String applicableTariff) {
		this.applicableTariff = applicableTariff;
	}
	public String getChecking() {
		return checking;
	}
	public void setChecking(String checking) {
		this.checking = checking;
	}
	
}
